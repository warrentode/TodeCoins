    /**
     * Returns the localized effect name for a given effect ID and locale.
     * Falls back to English ("en_us") if the locale is missing.
     * Falls back to a formatted effect ID if completely missing.
     */
    public static String getEffectName(String effectId, String locale) {
        Map<String, String> localeMap = LANG_MAPS.get(effectId);

        if (localeMap != null) {
            String name = localeMap.get(locale);
            if (name != null) return name;

            // fallback to English
            name = localeMap.get("en_us");
            if (name != null) return name;
        }

        // final fallback: format the effect ID
        return formatEffectId(effectId);
    }

    private static String formatEffectId(String effectId) {
        String[] words = effectId.split("_");
        StringBuilder sb = new StringBuilder();
        for (String word : words) {
            if (!word.isEmpty()) {
                sb.append(Character.toUpperCase(word.charAt(0)))
                        .append(word.substring(1))
                        .append(" ");
            }
        }
        return sb.toString().trim();
    }
